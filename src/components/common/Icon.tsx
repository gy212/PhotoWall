export function Icon({ name, size = 24, className, filled, ...props }: IconProps) {
    // Handle filled logic if the name doesn't already specify it
    // For 'favorite' -> 'favorite_filled', for 'star' -> 'star_filled'
    let finalName = name;
    if (filled) {
        if (name === 'favorite') finalName = 'favorite_filled';
        if (name === 'star') finalName = 'star_filled';
    }

    const IconComponent = icons[finalName];

    if (!IconComponent) {
        console.warn(`Icon "${finalName}" not found. Falling back to question mark.`);
        return null;
    }

    return (
        <svg
            width={size}
            height={size}
            viewBox="0 0 24 24"
            fill="currentColor"
            xmlns="http://www.w3.org/2000/svg"
            className={clsx('inline-block select-none flex-shrink-0', className)}
            {...props}
        >
            <IconComponent />
        </svg>
    );
}
